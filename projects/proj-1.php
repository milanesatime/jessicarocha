      <h3>Reality Teen</h3>
      <p>Reality Teen was created as part of a long-term study for an academic institution.  Teenagers taking place in the study would complete short Flash modules and then fill out questionnaires which they could choose to make public or only publish to their journals.</p>
      <p>Participants were given a code which doubled as a username and password which they used to log into the Wordpress front-end and see responses from other users in the study.  In addition, they could select a custom theme and avatar.</p>
      <p>A sidebar with available Flash modules, known as sessions, is shown.  As sessions expired, became available, or were completed, different styles were applied and older or unavailable sessions became unclickable.</p>
      <ul>
        <li>Wordpress</li>
        <li>Customized login pages for administrators and participants</li>
        <li>Custom queries to Wordpress database based on post type, category, and taxonomy</li>
        <li>Custom Wordpress theme created based on clients' design</li>
